import React from 'react';
import { NavigationContainer } from '@react-navigation/native';
import { createNativeStackNavigator } from '@react-navigation/native-stack';
import SplashScreen from './src/pages/SplashScreen';
import Login from './src/pages/Login';
import MainDashboard from './src/pages/MainDashboard';
import InnerDashboard from './src/pages/InnerDashboard';
import { Text, TextInput } from 'react-native';
const Stack = createNativeStackNavigator();

Text.defaultProps = Text.defaultProps || {};
Text.defaultProps.allowFontScaling = false;

TextInput.defaultProps = TextInput.defaultProps || {};
TextInput.defaultProps.allowFontScaling = false;

const App = () => {
  return (
    <NavigationContainer>
      <Stack.Navigator initialRouteName="Splash" screenOptions={{ headerShown: false }}>
        <Stack.Screen name="Splash" component={SplashScreen} />
        <Stack.Screen name="Login" component={Login} />
        <Stack.Screen name="MainDashboard" component={MainDashboard} />
        <Stack.Screen name="InnerDashboard" component={InnerDashboard} />
      </Stack.Navigator>
    </NavigationContainer>  
  );
};

export default App;
